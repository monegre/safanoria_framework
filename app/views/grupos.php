<h2>Grupos de Crianza</h2>

<p>Grupo flexible de madres con bebés de hasta 12 meses de edad. Este grupo está abierto a que puedan venir otros acompañantes, ya sean los hermanos mayores, la pareja u otros familiares.</p>

<p>Un espacio en el que se liberan las tensiones y vivencias íntimas que en otros espacios no tienen cabida. Cuidamos que la estructura física del grupo para que nadie se dé la espalda y tanto la comunicación verbal como la gestual tenga su lugar.</p>

<p>El grupo, aunque de carácter abierto y participativo, tiene unas normas para mantener un entorno seguro, e íntimo que garantiza el respeto entre sus miembros. Esto facilita la participación, la confianza y la cohesión grupal.</p> 

<p><strong>General</strong></p>
<p>Ayudar a resolver los problemas cotidianos entre madres y bebés de forma respetuosa.</p> 

<p><strong>Específicos</strong></p>
<ol>
	<li>Reforzar el vínculo entre madre e hijo favoreciendo el entendimiento entre ambos.</li>
	<li>Facilitar las diferentes vivencias preocupantes que conlleva la alimentación, el sueño y otras necesidades básicas de los recién nacidos.</li>
	<li>Ofrecer un espacio de reflexión y respeto en el que tengan cabida las múltiples formas respetuosas de criar y hacerse cargo de los hijos. </li>
	<li>Acompañar a las madres a una nueva situación vital y familiar.</li>
	<li>Desarrollar las propias capacidades de las madres para gestionar esta nueva situación.</li>
	<li>Favorecer una relación familiar saludable y amorosa en el que el respeto y la responsabilidad sean ejes vertebrales.</li>
	<li>Fomentar una red de apoyo y comunicación de estas familias estableciendo valores positivos entre los diferentes miembros de la comunidad.</li>
</ol>

<p><strong>Temas que trabajamos en los grupos</strong></p>
<ul>
	<li>La alimentación (lactancia materna, artificial y mixta)</li>
	<li>La hora de la comida. El pasaje a alimentos sólidos</li>
	<li>La organización del sueño del bebé</li>
	<li>La organización del tiempo y rutinas</li>
	<li>Como establecer  límites</li>
	<li>La comunicación</li>
	<li>La relación con la pareja</li>
	<li>La relación de los niños con el papá</li>
	<li>La relación con los abuelos y otra familia extensa</li>
	<li>Maternidad y la vuelta al trabajo</li>
	<li>El control de esfínteres. ¿Como ayudarlo a dejar los pañales?</li>
	<li>Los celos</li>
	<li>La llegada del hermanito/a</li>
	<li>La adaptación al jardín de infancia</li>
	<li>Como estimular adecuadamente al bebé</li>
	<li>La importancia del juego y del tiempo libre</li>
</ul>